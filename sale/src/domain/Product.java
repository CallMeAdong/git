package domain;

import java.io.File;
import java.util.Date;

public class Product {
	private int pid;//商品id
	private int uid;//卖家id
	private String pname;//商品名字
	private double price;//商品价格
	private String pimg;//商品图片
	private String kinds;//商品类型
	private Date date;//上架日期
	private Boolean issale;//是否卖出
	private String info;//商品描述
	private double counts;//点击数量
	private double number;//商品数量
	private String uphone;//卖家电话
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getPimg() {
		return pimg;
	}
	public void setPimg(String pimg) {
		this.pimg = pimg;
	}
	public String getKinds() {
		return kinds;
	}
	public void setKinds(String kinds) {
		this.kinds = kinds;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public Boolean getIssale() {
		return issale;
	}
	public void setIssale(Boolean issale) {
		this.issale = issale;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public double getCounts() {
		return counts;
	}
	public void setCounts(double counts) {
		this.counts = counts;
	}
	public double getNumber() {
		return number;
	}
	public void setNumber(double number) {
		this.number = number;
	}
	
	public String getUphone() {
		return uphone;
	}
	public void setUphone(String uphone) {
		this.uphone = uphone;
	}
	@Override
	public String toString() {
		return "Product [pid=" + pid + ", uid=" + uid + ", pname=" + pname + ", price=" + price + ", pimg=" + pimg
				+ ", kinds=" + kinds + ", date=" + date + ", issale=" + issale + ", info=" + info + ", counts=" + counts
				+ ", number=" + number + ", uphone=" + uphone + "]";
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
}
